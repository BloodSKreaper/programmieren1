package blatt05;

/** Beispiel für Referenzen: Klasse für Mitarbeiter-Raumzuordnung */
public class Raumzuordnung {
	private Raum raum; // Referenz auf Raum
	private Person nutzer; // Referenz auf Nutzer/in
	private String zuordnungsGrund; // Grund für die Zuordnung

	/** Konstruktor */
	public Raumzuordnung(Raum raum, Person nutzer, String zuordnungsGrund) {
		this(raum, nutzer);
		if (zuordnungsGrund != null && zuordnungsGrund.trim().length() >= 3) {
			this.zuordnungsGrund = zuordnungsGrund;
		} else {
			// ESKALATION, aber wie nur?
			System.err.println("Ungültiger Grund für die Zuordnung");
		}

	}

	/** Konstruktor */
	public Raumzuordnung(Raum raum, Person nutzer) {
		if (raum != null && nutzer != null) {
			this.raum = raum;
			this.nutzer = nutzer;
		} else {
			// MAXIMALE ESKALATION
			throw new IllegalArgumentException("Raum und Nutzer müssen dürfen nicht NULL sein");
		}
	}

	public void print() {
		this.getNutzer().print();
		System.out.print(" nutzt den Raum: ");
		this.getRaum().print();
		if (zuordnungsGrund != null) {
			System.out.print(" als "+zuordnungsGrund);
		}
	}

	Raum getRaum() {
		return this.raum;
	}

	Person getNutzer() {
		return this.nutzer;
	}

	String getZuordnungsGrund() {
		return zuordnungsGrund;
	}

	public static void main(String[] a) {
		// Bröckl nutzt den Raum E 212a
		Person ub = new Person("Ulrich", "Bröckl");
		Raum e212a = new Raum("e 212a", 2);
		Raumzuordnung ub2e212a = new Raumzuordnung(e212a, ub, "Büro");
		ub2e212a.print();

		// Bröckl nutzt außerdem den Raum E 201
		Raumzuordnung ub2e201 = new Raumzuordnung(new Raum("e 201", 77, true), // keine Zwischenvariable für e201
																				// nötig...
				ub);
		// Den Raum dieser Zuordnung ausgeben:
		System.out.print("\n     Außerdem: ");
		ub2e201.getRaum().print();
		System.out.println("\n");

		// Vogelsang nutzt den Raum E 209
		Raumzuordnung hv2e209 = new Raumzuordnung(new Raum("e 209", 3), new Person("Holger", "Vogelsang"), "Büro");
		hv2e209.print();

	}

}